const LOCALE_MESSAGES: Record<AppLocale, LocaleCatalog> = {
  en: EN_MESSAGES,
  fr: FR_MESSAGES,
  de: DE_MESSAGES,
  es: ES_MESSAGES,
  it: IT_MESSAGES,
  nl: NL_MESSAGES,
}

export function isAppLocale(value: string): value is AppLocale {
  return SUPPORTED_LOCALES.includes(value as AppLocale)
}

export function translate(locale: AppLocale, key: TranslationKey, vars?: TranslationVars): string {
  const template = LOCALE_MESSAGES[locale][key] ?? EN_MESSAGES[key]
  if (!vars) {
    return template
  }
  return template.replace(/\{([a-zA-Z0-9_]+)\}/g, (_, token: string) => {
    const value = vars[token]
    return value === undefined ? `{${token}}` : String(value)
  })
}
